package osu.serverlist.Sites.Models;

public class BanchoPyStats {

    private int id;
    private int maps;
    private int plays;
    private int clans;
    

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMaps() {
        return this.maps;
    }

    public void setMaps(int maps) {
        this.maps = maps;
    }

    public int getPlays() {
        return this.plays;
    }

    public void setPlays(int plays) {
        this.plays = plays;
    }

    public int getClans() {
        return this.clans;
    }

    public void setClans(int clans) {
        this.clans = clans;
    }

}
